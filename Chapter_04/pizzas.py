# 1. 定义比萨列表
pizzas = ['Margherita', 'Pepperoni', 'Hawaiian', 'Veggie']

# 2. 使用 for 循环遍历列表
for pizza in pizzas:
    # 打印比萨名称
    print(pizza)

    # 修改循环，打印包含名称的句子 (使用 f-string)
    print(f"I like {pizza} pizza!\n")
    
# 3. 在循环结束后添加一行代码 (注意没有缩进)
print("I really love pizza!")

