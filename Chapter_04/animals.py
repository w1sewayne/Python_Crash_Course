# 1. 定义动物列表
animals = ["cat", "dog", "rabbit", "hamster", "parrot"]

# 2. 使用 for 循环遍历列表
for animal in animals:
    # 打印动物名称
    print(animal)

    # 修改循环，打印包含名称的句子 (使用 f-string)
    print(f"A {animal} would make a great pet!\n")

# 3. 在循环结束后添加一行代码 (注意没有缩进)
print("Any of these animals would make a wonderful pet!")

